package day3;

public class For2 {

	public static void main(String[] args) {
		/* 1부터 5까지 출력하는 예제를 작성하세요 
		 * 반복횟수 : i는 1부터 5까지 1씩 증가
		 * 규칙성 : i를 출력
		 * 초기화 : i = 1 부터
		 * 조건식 : i <= 5 까지
		 * 증감식 : i++ 1씩 증가
		 * 실행문 : sysout(i)
		 * */

		int i;
		for(i=1; i<=5; i++) {
			System.out.println(i);
		}
		

}
}
