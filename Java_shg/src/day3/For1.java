package day3;

public class For1 {

	public static void main(String[] args) {
		/* Hello world 5번 for문으로 출력하는 예제*/
		
		int i;
		for(i = 1; i<=5; i++) {
			System.out.println("Hello world");
		
			}
		/* for문에는 규칙성과 반복횟수가 힌트 */
		
		/* 반복횟수 : i는 1부터 5까지 1씩 중가 -> 반복횟수를 보고 [ 초기화, 조건식, 증감식 ] 을 결정
		 * 			초기화 : ~ 부터 
		 * 			조건식 : ~ 까지
		 * 			증감식 : ~ 씩 증가 / ~ 씩 감소
		 * 규칙성 : Hello world 를 콘솔에 출력
		 *  
		 *  
		 * for(초기화; 조건식; 증감식) {
		 *  							실행문;
		 *  }
		 * */
		
		
		
		}

	}

